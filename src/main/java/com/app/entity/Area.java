package com.app.entity;

public class Area {

    private Integer id;
    private String area_name;
    private String address;
    private String join_time;
    private Integer type;
    private String footprint;
    private Integer unit_num;
    private Integer home_num;
    private Integer status;
    private String comments;
    private String extend1;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getArea_name() {
        return area_name;
    }

    public void setArea_name(String area_name) {
        this.area_name = area_name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getJoin_time() {
        return join_time;
    }

    public void setJoin_time(String join_time) {
        this.join_time = join_time;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getFootprint() {
        return footprint;
    }

    public void setFootprint(String footprint) {
        this.footprint = footprint;
    }

    public Integer getUnit_num() {
        return unit_num;
    }

    public void setUnit_num(Integer unit_num) {
        this.unit_num = unit_num;
    }

    public Integer getHome_num() {
        return home_num;
    }

    public void setHome_num(Integer home_num) {
        this.home_num = home_num;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }

    public String getExtend1() {
        return extend1;
    }

    public void setExtend1(String extend1) {
        this.extend1 = extend1;
    }
}
